#include<iostream>
#include<math.h>

using namespace std;

int main()
{
	int l,h,x=0,i;
	cout<<"Dame el numero mas bajo a sumar"<<endl;
	cin>>l;
	cout<<"Dame el numero mas alto a sumar"<<endl;
	cin>>h;

	for(i==l;i<h;i++)
	{
		x=x+i;
	}

	cout<<"El resultado de la suma del "<<l<<" al "<<h<<" es de: "<<x<<endl;
	return 0;
}
