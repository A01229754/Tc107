#include<iostream>
#include<math.h>
using namespace std;
int main()
{
	int a,b;
	cout<<"Dame un primer numero a introducir: ";
	cin>>a;
	cout<<"Dame un segundo numero a introducir: ";
	cin>>b;
	cout<<"La suma de los 2 numeros es: "<<a+b<<endl;
	cout<<"La resta de los 2 numeros es: "<<a-b<<endl;
	cout<<"La division de los 2 numeros es: "<<a/b<<endl;
	cout<<"El residuo de la division entre los 2 numeros es: "<<a%b<<endl;

	return 0;
}
